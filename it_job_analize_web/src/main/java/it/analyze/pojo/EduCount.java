package it.analyze.pojo;

public class EduCount {
	private String edu;
	private int num;
	public EduCount(String edu, int num) {
		this.edu = edu;
		this.num = num;
	}
	public String getEdu() {
		return edu;
	}
	public void setEdu(String edu) {
		this.edu = edu;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) { 
		this.num = num;
	}
}
